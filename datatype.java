public class datatype {
    public static void main(String[] args) {
        byte b = 8;
        System.out.println(b);
        char ch = 'a';
        System.out.println(ch);
        boolean var = true;
        System.out.println(var);
        float price = (float) 10.5;
        System.out.println(price);
        int no = 5;
        short n = 20;
    }
    
}
