public class assignmentOperators {
    public static void main(String[] args) {
        int A = 10;
        A = A + 5;
        System.out.println(A);

        int B = 10;
        B += 5;
        System.out.println(B);

        A = A - 5;
        System.out.println(A);
        B -= 5;
        System.out.println(B);

        A = A * 5;
        System.out.println(A);
        B *= 5;
        System.out.println(B);

        A = A / 10;
        System.out.println(A);
        B = B/10;
        System.out.println(B);

        A = A % 2;
        System.out.println(A);
        B %= 2;
        System.out.println(B);
    }
}
